"""End-to-end regression tests for the Round-4 expanded test corpus.

These tests cover the three NEW fixtures generated by
`tests/fixtures/build_corpus.py`:

    - logistics_routing_synth.xlsm    -> domain `logistics-routing`
    - inventory_supply_synth.xlsm     -> domain `inventory-supply-chain`
    - minimal_no_vba.xlsm             -> domain `unknown` (degradation)

For each fixture we assert:
    1. The audit completes (rc == 0).
    2. Idempotency holds (two runs yield byte-identical md + json).
    3. The detected domain matches expectation.
    4. The complexity score sits inside an expected ±5 band.
    5. A specific expected pillar (or the absence of pillars) is present.
    6. A specific expected workflow signal (button vs. graceful-empty).
    7. All H2 sections that should be there ARE there.

The number ranges below are documented in `docs/test-corpus.md` and serve as
the canonical regression baseline going forward. If any of these starts
diverging, either the audit logic changed (intentionally — bump the range)
or the corpus changed (un-intended — investigate before tweaking the test).
"""

from __future__ import annotations

import json
from pathlib import Path

import pytest

from tier1_audit.cli import main as cli_main


REPO_ROOT = Path(__file__).resolve().parents[1]
FIXTURES_DIR = Path(__file__).resolve().parent / "fixtures"

LOGISTICS_FIXTURE = FIXTURES_DIR / "logistics_routing_synth.xlsm"
INVENTORY_FIXTURE = FIXTURES_DIR / "inventory_supply_synth.xlsm"
MINIMAL_FIXTURE   = FIXTURES_DIR / "minimal_no_vba.xlsm"


def _run_audit(input_path: Path, out_dir: Path):
    out_dir.mkdir(parents=True, exist_ok=True)
    rc = cli_main([str(input_path), "--out-dir", str(out_dir)])
    md = (out_dir / "audit.md").read_text(encoding="utf-8")
    js_text = (out_dir / "audit.json").read_text(encoding="utf-8")
    return rc, md, js_text


def _load_json(out_dir: Path) -> dict:
    return json.loads((out_dir / "audit.json").read_text(encoding="utf-8"))


# Required H2 sections that must always be present, regardless of fixture.
# Note: "Domain-Specific Findings" is intentionally CONDITIONAL — it only
# renders when at least one domain template matches with high/medium
# confidence. We assert that separately per-fixture.
_REQUIRED_H2_SECTIONS = (
    "## Executive Summary",
    "## Table of Contents",
    "## Workflow Guide",
    "## Data Flow Story",
    "## Top Impact Findings",
    "## VBA Module Walkthrough",
    "## Reference Appendix",
    "## Glossary",
    "## Methodology",
)


def _assert_required_sections(md: str, fixture_name: str):
    for sec in _REQUIRED_H2_SECTIONS:
        assert sec in md, f"{fixture_name}: missing required section {sec!r}"


