def test_logistics_audit_completes(tmp_path: Path):
    if not LOGISTICS_FIXTURE.exists():
        pytest.skip("logistics fixture not built — run build_corpus.py")
    rc, md, _ = _run_audit(LOGISTICS_FIXTURE, tmp_path / "out")
    assert rc == 0
    assert md.startswith("# Audit report")
    _assert_required_sections(md, "logistics_routing_synth")


def test_logistics_idempotent(tmp_path: Path):
    if not LOGISTICS_FIXTURE.exists():
        pytest.skip("logistics fixture not built")
    out1 = tmp_path / "run1"
    out2 = tmp_path / "run2"
    rc1, md1, js1 = _run_audit(LOGISTICS_FIXTURE, out1)
    rc2, md2, js2 = _run_audit(LOGISTICS_FIXTURE, out2)
    assert rc1 == 0 and rc2 == 0
    assert md1 == md2, "logistics audit.md should be byte-identical"
    assert js1 == js2, "logistics audit.json should be byte-identical"


def test_logistics_domain_detected(tmp_path: Path):
    if not LOGISTICS_FIXTURE.exists():
        pytest.skip("logistics fixture not built")
    _run_audit(LOGISTICS_FIXTURE, tmp_path / "out")
    j = _load_json(tmp_path / "out")
    dh = j.get("domain_hint") or {}
    assert dh.get("domain") == "logistics-routing", \
        f"expected logistics-routing, got {dh.get('domain')}"
    assert dh.get("confidence") in {"high", "medium"}, \
        f"expected high/medium confidence, got {dh.get('confidence')}"
    # Template-level: logistics-routing template should fire at high/medium
    matches = j.get("domain_template_matches") or []
    keys_conf = {m["template_key"]: m["confidence"] for m in matches}
    assert "logistics-routing" in keys_conf, \
        f"logistics template should match, got {list(keys_conf)}"
    assert keys_conf["logistics-routing"] in {"high", "medium"}, \
        f"logistics template confidence {keys_conf['logistics-routing']!r}"


def test_logistics_complexity_in_range(tmp_path: Path):
    if not LOGISTICS_FIXTURE.exists():
        pytest.skip("logistics fixture not built")
    _run_audit(LOGISTICS_FIXTURE, tmp_path / "out")
    j = _load_json(tmp_path / "out")
    score = j["complexity"]["total"]
    assert 63 <= score <= 73, f"logistics complexity {score} outside [63,73]"


def test_logistics_expected_pillar_routes(tmp_path: Path):
    """The Routes sheet's RouteID column (A2..A31) is a pillar:
    each cell is referenced by 1 SUMIFS in Routes!F* + ~99 INDEX/MATCH
    in Shipments!E*. Fan-in >= 20 is the threshold."""
    if not LOGISTICS_FIXTURE.exists():
        pytest.skip("logistics fixture not built")
    _run_audit(LOGISTICS_FIXTURE, tmp_path / "out")
    j = _load_json(tmp_path / "out")
    pillars = j.get("pillars") or []
    assert pillars, "logistics fixture should have at least 1 pillar"
    # At least one pillar should be a Routes!A column-block group
    routes_pillar = next(
        (p for p in pillars
         if p.get("pillar_kind") == "column-block" and "Routes!A" in p["location"]),
        None,
    )
    assert routes_pillar is not None, \
        f"expected a Routes!A column-block pillar; got: {[p['location'] for p in pillars]}"
    assert routes_pillar["fan_in"] >= 20


def test_logistics_button_detected(tmp_path: Path):
    """A real form-control button bound to `[0]!Execute` is injected on
    the Routes sheet. The workflow detector should pick it up."""
    if not LOGISTICS_FIXTURE.exists():
        pytest.skip("logistics fixture not built")
    rc, md, _ = _run_audit(LOGISTICS_FIXTURE, tmp_path / "out")
    assert rc == 0
    j = _load_json(tmp_path / "out")
    wf = j.get("workflow") or {}
    btns = wf.get("buttons") or []
    assert len(btns) >= 1, f"logistics should have >= 1 button, got {btns}"
    # The button macro should match what we injected
    assert any("Execute" in b.get("macro", "") for b in btns), \
        f"expected an 'Execute' macro button, got: {btns}"
    # Workflow Guide should reference the button label
    assert "Run Routing Calculation" in md, \
        "Routes button label should appear in markdown"


