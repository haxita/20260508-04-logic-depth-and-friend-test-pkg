NEW_FIXTURES = [
    ("logistics_routing_synth", LOGISTICS_FIXTURE, "logistics-routing"),
    ("inventory_supply_synth", INVENTORY_FIXTURE, "inventory-supply-chain"),
    ("minimal_no_vba",          MINIMAL_FIXTURE,   "unknown"),
]


@pytest.mark.parametrize("name,path,_expected_domain", NEW_FIXTURES)
def test_new_corpus_runs_clean(tmp_path: Path, name: str, path: Path, _expected_domain: str):
    if not path.exists():
        pytest.skip(f"{name} fixture not built — run build_corpus.py")
    rc, md, js = _run_audit(path, tmp_path / "out")
    assert rc == 0
    j = json.loads(js)
    assert j["meta"]["file_name"] == path.name
    # Always emits valid JSON with the structural fields the renderer expects
    for key in ("basic_stats", "complexity", "smells", "pillars",
                "anomalies", "vba_modules", "vba_classifications",
                "workflow", "vba_narratives", "domain_hint",
                "domain_template_matches", "methodology"):
        assert key in j, f"{name}: missing JSON key {key}"


@pytest.mark.parametrize("name,path,expected_domain", NEW_FIXTURES)
def test_new_corpus_domain_match(tmp_path: Path, name: str, path: Path, expected_domain: str):
    if not path.exists():
        pytest.skip(f"{name} fixture not built")
    _run_audit(path, tmp_path / "out")
    j = _load_json(tmp_path / "out")
    actual = (j.get("domain_hint") or {}).get("domain", "unknown")
    assert actual == expected_domain, \
        f"{name}: expected domain {expected_domain!r}, got {actual!r}"
